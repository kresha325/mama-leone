function resolve<T>(map: Record<Language, T>, lang: Language, key: string | number, fallback: string): string {
  const langMap = map[lang] as Record<string | number, string> | undefined;
  const deMap = map.de as Record<string | number, string>;
  return langMap?.[key] ?? deMap?.[key] ?? fallback;
}

